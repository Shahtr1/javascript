/*
    Object oriented programming says that bringing together the data and its behaviour in a single location called an object
    and contaning all that in a box makes it easier to understand how it works

    Objects are first class citizens

    Few operations on common data

    Stateful

    has side-effects(methods manipulating the internal state)

    imperative

    not too many operations

*/

/*
    Functional programming says, data and behaviour are distinctly different things and should be separate
    (give me the data i will return sth new)

    there are pure functions

    Functions are first class citizens

    Many different operations for which the data is fixed

    Immutable state (stateless)

    no side-effects (pure)

    declarative

    for large data, can run in multiple processors
*/
