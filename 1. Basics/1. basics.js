// Functions
// ------------
// In js, functions are variables.

// JS Terminology
// --------------
// function declaration
function newFunction() {}

// Function expression
// ----------------------
var newFunction = function () {
  // anonymous function
};
var newFunction = function name() {
  // named function
};

// Expression (sth that produces a value)
// -----------------------------------------
1 + 3;
var a = 2;
return true;

// Calling or invoking a function
// ---------------------------------
alert();
newFunction(param1, param2);

// Assign a variable
// --------------------
var a = true;

// Function vs Methods
// ----------------------
function thisIsAFunction() {}

var obj = {
  thisIsAMethod: function () {
    // inside an object
  },
};

